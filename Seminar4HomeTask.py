# В первом списке находится информация об ассортименте мороженого, во втором списке – информация о том, какое мороженое есть на складе. Выведите названия того товара, который закончился.
# Пример:
# 1 строка файла – сливочное, бурёнка, вафелька, сладкоежка
# 2 строка файла – сливочное, вафелька, сладкоежка
# Ответ: закончилось – бурёнка

def task2():
    products = ['Сливочное', 'Бурёнка', 'Вафелька', 'Сладкоежка']
    stock = ['Сливочное', 'Вафелька']
