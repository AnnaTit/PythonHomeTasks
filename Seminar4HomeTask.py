# Задача 1
# Дано натуральное число N. Напишите метод, который вернёт список простых множителей числа N и количество этих множителей

def task1():
    def primeCheck(n):
        if n % 2 == 0:
            return n == 2
        a = 3
        while a * a <= n and n % a != 0:
            a += 2
        return a * a > n 

    def primeMulti(n):
        a = []
        for i in range(2, n):
            if primeCheck(i):
                while n % i == 0:
                    a.append(f'{i}, ')
                    n/=i 
        return a 

    primeMultiRes = primeMulti(int(input("Введите число: ")))
    print(f"Количество простых множителей: {len(primeMultiRes)}. \nПростые множители: {''.join(primeMultiRes)}")


